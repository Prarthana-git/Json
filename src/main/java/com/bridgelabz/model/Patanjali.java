package com.bridgelabz.model;

public class Patanjali {
	private String stock_name;
	private int number_of_share;
	private int share_price;
	private int totalValueOfStock;
	
	public String getStock_name() {
		return stock_name;
	}
	public void setStock_name(String stock_name) {
		this.stock_name = stock_name;
	}
	public int getNumber_of_share() {
		return number_of_share;
	}
	public void setNumber_of_share(int number_of_share) {
		this.number_of_share = number_of_share;
	}
	public int getShare_price() {
		return share_price;
	}
	public void setShare_price(int share_price) {
		this.share_price = share_price;
	}
	public int getTotalValueOfStock() {
		return totalValueOfStock;
	}
	public void setTotalValueOfStock(int totalValueOfStock) {
		this.totalValueOfStock = totalValueOfStock;
	}
	

}
